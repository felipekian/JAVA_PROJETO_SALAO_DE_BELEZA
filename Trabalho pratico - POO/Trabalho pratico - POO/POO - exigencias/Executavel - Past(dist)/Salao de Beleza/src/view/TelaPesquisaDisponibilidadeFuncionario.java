    private void jvoltar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jvoltar3ActionPerformed
        //Quais hor√°rios um profissional tem dispon√≠vel num determinado dia;
        
        //pega a data do sistema operacional
       // SimpleDateFormat formatoBrasileiro = new SimpleDateFormat("dd/MM/yyyy");
        //Date d = new Date();        
       // String data = formatoBrasileiro.format(d);
        
        //abre as coneccoes com o banco
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        
        
        //verificar o select*************************
        try {
            stmt = (PreparedStatement) con.prepareStatement("SELECT servico.horario as hora from servico, funcionario where servico.id_func=funcionario.cpf and servico.calendario=? and funcionario.cpf=? ORDER BY horario ASC");
            stmt.setString(1, txtData.getText());
            stmt.setInt(2, Integer.parseInt(txtCPF.getText()));
            rs = stmt.executeQuery();
            
            DefaultTableModel modelo = (DefaultTableModel) jTableHorariosDisponiveis.getModel();
            modelo.setNumRows(0);
            int [] vet = new int[21];
            
            for(int i=0;i<=20;i++){
                vet[i]=0;
            }
            
            while(rs.next()){
                
                int n = rs.getInt("hora");                
                vet[n]=1;
                
            }
            
            for(int i=8;i<=20;i++){
                
                if(vet[i]==0){    
                    
                    String s = Integer.toString(i);
                            s = s+" horas";
                            s = "                                     "+s;
                    modelo.addRow(new String[]{
                        s             
                    });

                }
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaPesquisaDisponibilidadeFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            ConnectionFactory.closeConnection(con, stmt, rs);
        }
        
        
    }//GEN-LAST:event_jvoltar3ActionPerformed

