    private void jvoltar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jvoltar3ActionPerformed
        // Qual hor√°rio o cliente est√° marcado para aquele dia, qual procedimento ele ir√° realizar e qual profissional prestar√° o servi√ßo
        
        //pega a data do sistema operacional
        SimpleDateFormat formatoBrasileiro = new SimpleDateFormat("dd/MM/yyyy");
        Date d = new Date();        
        String data = formatoBrasileiro.format(d);
        
        
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        
        DefaultTableModel modelo = (DefaultTableModel) JTableResult.getModel();
        modelo.setNumRows(0);        
        
        try {
            
            // Qual hor√°rio o cliente est√° marcado para aquele dia, qual procedimento ele ir√° realizar e qual profissional prestar√° o servi√ßo
            stmt = (PreparedStatement) con.prepareStatement("SELECT servico.horario as hora, funcionario.especialidade as especFunc , funcionario.nome as nomeFunc FROM cliente , servico , funcionario WHERE servico.id_cliente=cliente.cpf and servico.id_func=funcionario.cpf and servico.calendario=? and cliente.cpf=?;");
                                    
            stmt.setString(1,data);
            stmt.setInt(2, Integer.parseInt(txtCPF.getText()));
                       
            rs = stmt.executeQuery();
                        
            while(rs.next()){
                          
                 Servico c = new Servico();
                 
                 c.setHorario(rs.getInt("hora"));
                 c.setEspecialidadeFunc(rs.getString("especFunc"));
                 c.setNomeFuncionario(rs.getString("nomeFunc"));
                                 
                 
                modelo.addRow(new Object[]{
                    c.getHorario(),
                    c.getEspecialidadeFunc(),
                    c.getNomeFuncionario()
                    
                });
            
            }
                    
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaPesquisarCliente.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            ConnectionFactory.closeConnection(con, stmt, rs);
            
        }              
        
        
    }//GEN-LAST:event_jvoltar3ActionPerformed

